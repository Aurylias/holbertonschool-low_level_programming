#include "3-calc.h"

/**
* op_add - Add two int together
* @a: First integer
* @b: Second integer
* Return: The sum of a by b
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - Subtract two int together
* @a: First integer
* @b: Second integer
* Return: The subtraction of a by b
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - Multiply two int together
* @a: First integer
* @b: Second integer
* Return: The multiplication of a by b
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - Divide two int together
* @a: First integer
* @b: Second integer
* Return: The division of a by b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - Get the modulo two int together
* @a: First integer
* @b: Second integer
* Return: The modulo of a by b
*/

int op_mod(int a, int b)
{
	return (a % b);
}